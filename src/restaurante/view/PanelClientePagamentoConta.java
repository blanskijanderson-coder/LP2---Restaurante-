    public PanelClientePagamentoConta(Cliente pessoaLogada, javax.swing.JTabbedPane BarraTarefas) {
        initComponents();
        this.BarraTarefas = BarraTarefas;
        this.usuarioLogado = pessoaLogada;
        
        DefaultTableModel TabelaClienteContaPedidosFeitos = (DefaultTableModel) tblClienteContaPedidosFeitos.getModel();
        
        int cont = 0;
        
        for(Pedido itens : usuarioLogado.getContaAtual().getListaPedidosCliente()){
           
            cont ++;
            itens.setOrdem(cont);
            
            Object[] listaPedidos = new Object[]{itens, itens.getStatus(), "$" + itens.getTotal()};
            TabelaClienteContaPedidosFeitos.addRow(listaPedidos);
        }
        
        double totalConta = usuarioLogado.getContaAtual().getTotalConta();
        double bonus = usuarioLogado.getBonus();
        double totalComDesconto = totalConta - bonus;
        if(totalComDesconto < 0){ totalComDesconto = 0; }

        lblClientePagarContaTotal.setText("Total: $" + totalComDesconto);
        txtBonusInfo.setText("Bônus de compra abatido do pagamento: R$ " + bonus);
    }

